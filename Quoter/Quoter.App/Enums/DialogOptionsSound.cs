namespace Quoter.App.Enums
{
	public enum DialogOptionsSound
	{
		/// <summary>
		/// Don't play any sound when opening the dialog
		/// </summary>
		None = 0,
		/// <summary>
		/// Play the default notification sound selected by the user from settings
		/// </summary>
		Default = 1,
		/// <summary>
		/// Play a warning sound (Asterix from windows)
		/// </summary>
		Warning = 2
	}
}
